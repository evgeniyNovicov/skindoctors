                        <p>
                            <strong>25 февраля 2025 года Трамп объявил о своем намерении закрыть программу для EB-5 и заменить ее визой «Золотая карта»</strong>
                            , требующей инвестиций в размере 5 миллионов долларов. Хотя это заявление попало в заголовки, оно выглядит очередным заявлением на публику.
                        </p>
                        <p>
                            Виза Трампа «Золотая карта» будет отдельной категорией, предназначенной для привлечения сверхбогатых, потенциально с такими преимуществами, как освобождение от мирового подоходного налога. Эта концепция, если она будет реализована, может сосуществовать с EB-5, а не заменять ее, создавая альтернативный путь для инвесторов с высоким уровнем дохода.
                        </p>
                        <p>
                            В настоящее время программа EB-5 приносит около 5 миллиардов долларов в год, финансируя предприятия и инфраструктуру США, создавая тысячи рабочих мест.
                            Заявление Трампа о том, что его предлагаемая виза может помочь сократить национальный дефицит в размере 34 триллионов долларов, просто нереально. Даже если бы новая программа привлекла значительные инвестиции, ее вклад был бы каплей в море по сравнению с масштабом дефицита. 
                        </p>
                        <p>
                            <strong>Может ли Трамп положить конец EB-5? </strong>
                        </p>
                        <p>
                            Несмотря на заявление, Трамп не может в одностороннем порядке отменить программу EB-5. Иммиграционное законодательство контролируется Конгрессом (статья I, раздел 8 Конституции). В 2022 году Конгресс модернизировал и продлил EB-5 до 2027 года в соответствии с Законом о реформе EB-5 (RIA). Это означает, что любые усилия по отмене EB-5 потребуют принятия нового законодательства.
                        </p>
                        <p>
                            Хотя президент может влиять на иммиграционное правоприменение, прямое прекращение EB-5 посредством исполнительных действий немедленно столкнется с непреодолимыми юридическими проблемами.
                        </p>
                        <p>
                            <strong>Перспективы</strong>
                        </p>
                        <p>
                            Заявление Трампа, по-видимому, является шагом к расширению и ребрендингу иммиграции в США на основе инвестиций, а не намерением отменить ЕВ-5. Его администрация может настаивать на двойной системе — сохранении EB-5 для привлечения дешевого капитала в экономику США и создания рабочих мест, и введении визы «Золотая карта» для сверхбогатых на пока неизвестных условиях. Учитывая расстновку сил в конгрессе, отмена федерального закона о ЕВ-5 представляется нереалистичным, и EB-5 останется в силе как минимум до сентября 2027 года.
